                    <p class="timeline__content-desc">1714</p>
                    <p class="timeline__content-desc">Avec d'autres États, le parlement anglais mais au concours "toutes méthodes capable de déterminer la longitude en mer" avec une prime de 10 000 livres pour un résultat n'excédant pas 1° d'erreur, 15 000 livres pour 40 min et 20 000 livres pour 1/2° ou moins</p>
                </div>
