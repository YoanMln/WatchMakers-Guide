                    <p class="timeline__content-desc">Après la révocation de l’édit de Nantes en 1685, de nombreux huguenots se réfugient à Genève, apportant avec eux leur savoir-faire, notamment en horlogerie. Dans une cité marquée par la rigueur calviniste, les bijoutiers en déclin se reconvertissent en fabriquant des boîtes de montre. C’est ainsi que naît l’horlogerie suisse.</p>
