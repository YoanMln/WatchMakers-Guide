        <div class="timeline-header">
            <h1 class="timeline-header__title">Histoire de la mesure du temps</h1>
            <h2 class="timeline-header__subtitle">Des premières civilisations au monde moderne,<br> la mesure du temps a été essentielle pour organiser la vie sociale, religieuse et économique. <br> D’abord basée sur l’observation des astres, elle s’est peu à peu perfectionnée grâce à l’invention de moyens toujours plus précis <br> pour se repérer dans le temps.</h2>
        </div>

        <div class="timeline">
            <div class="timeline-item" data-text="Histoire de la mesure du temps">
                <div class="timeline__content">
                    <img class="timeline__img" src="assets/images/mesure-du-temps/Gnonom.webp" alt="gnomon ancien" />
                    <h2 class="timeline__content-title">Le gnomon </h2>
                    <h3 class="timeline__content-date">IIIème millénaire av J.-C</h3>
                    <p class="timeline__content-desc">C'est le plus ancien instrument à mesurer le temps. Il est formé d'un style vertical qui peut être aussi : un tronc d'arbre, en colonne, une aiguille de pierre, dont l'ombre projetée sur la terre sert à la détermination du temps. l'ombre la plus courte indique le midi vrai. Les différentes directions de l'ombre permettent de diviser la journée en périodes égales</p>
                </div>
            </div>

            <div class="timeline-item" data-text="Histoire de la mesure du temps">
                <div class="timeline__content">
                    <img class="timeline__img" src="assets/images/mesure-du-temps/Cadran-solaire.webp" alt="cadran solaire" />
                    <h2 class="timeline__content-title">Le cadran solaire</h2>
                    <h3 class="timeline__content-date">1500 av J.-C</h3>
                    <p class="timeline__content-desc"> Instrument de même nature que le gnomon, mais plus perfectionné, il peut être horizontal ou vertical. Le style est parallèle à l'axe de rotation de la terre.</p>
                </div>
            </div>

            <div class="timeline-item" data-text="Histoire de la mesure du temps">
                <div class="timeline__content">
                    <img class="timeline__img" src="assets/images/mesure-du-temps/Clepsydre.webp" alt="Clépsydre" />
                    <h2 class="timeline__content-title">La clépsydre</h2>
                    <h3 class="timeline__content-date">Partie d'Egypte en 1500 av J.-C, la clépsydre s’est ensuite répandue chez les Romains en 159 av J.-C </h3>
                    <p class="timeline__content-desc"> Garde-temps constitué d'un récipient rempli d'eau avec un trou à sa partie inférieure. À mesure que l'eau s'écoule par le trou, son niveau dans le récipient baisse. Ce niveau indique les différentes heures de la journée.</p>
                </div>
            </div>

            <div class="timeline-item" data-text="Histoire de la mesure du temps">
                <div class="timeline__content">
                    <img class="timeline__img" src="assets/images/mesure-du-temps/Bougie.webp" alt="bougie graduée" />
                    <h2 class="timeline__content-title">La bougie graduée</h2>
                    <h3 class="timeline__content-date">Vers 870</h3>
                    <p class="timeline__content-desc"> La bougie ou chandelle horaire, pouvait servir à une double utilisation, celle d'éclairer, mais aussi en étant graduée, elle pouvait mesurer le temps</p>
                </div>
            </div>

            <div class="timeline-item" data-text="Histoire de la mesure du temps">
                <div class="timeline__content">
                    <img class="timeline__img" src="assets/images/mesure-du-temps/Foliot.webp" alt="horloge à Foliot" />
                    <h2 class="timeline__content-title">Horloges à Foliot</h2>
                    <h3 class="timeline__content-date">1370</h3>
                    <p class="timeline__content-desc"> La véritable horloge mécanique apparaît. Un poids suspendu à une corde fournit de l'énergie à la machine, tandis qu'un système de régulation, appelé verge et foliot, interrompt régulièrement la chute du poids. N'étant pas très précise, elle possède qu'une aiguille, celle des heures</p>
                </div>
            </div>

            <div class="timeline-item" data-text="Histoire de la mesure du temps">
                <div class="timeline__content">
                    <img class="timeline__img" src="assets/images/mesure-du-temps/ressort-barillet.webp" alt=" ressort de barillet" />
                    <h2 class="timeline__content-title">Le ressort de barillet</h2>
                    <h3 class="timeline__content-date">1430</h3>
                    <p class="timeline__content-desc"> Le ressort, logé dans un barillet fut appliqué aux horloges vers 1430, la montre avec moteur fait son apparition vers 1500</p>
                </div>
            </div>

