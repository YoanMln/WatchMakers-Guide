                    <img class="timeline__img" src="assets/images/mesure-du-temps/longitude.webp" alt="longitude" />
                    <h2 class="timeline__content-title">Calcul de la longitude</h2>
                    <h3 class="timeline__content-date">1714</h3>
                    <p class="timeline__content-desc">Avec d'autres États, le parlement anglais met au concours "toutes méthodes capable de déterminer la longitude en mer" avec une prime de 10 000 livres pour un résultat n'excédant pas 1° d'erreur, 15 000 livres pour 40 min et 20 000 livres pour 1/2° ou moins</p>
                </div>
            </div>

            <div class="timeline-item" data-text="Histoire de la mesure du temps">
                <div class="timeline__content">
                    <img class="timeline__img" src="assets/images/mesure-du-temps/compensateur.webp" alt="pendule compensateur" />
                    <h2 class="timeline__content-title">Le pendule compensateur</h2>
                    <h3 class="timeline__content-date">1725</h3>
                    <p class="timeline__content-desc">Inventé pour corriger les effets de la dilatation thermique sur la longueur du pendule, qui influence la précision des horloges. Il est composé de deux métaux aux coefficients de dilatation différents, de manière à ce que leurs expansions se compensent. Ce type de pendule est notamment associé à John Harrison, qui a aussi développé d’autres dispositifs pour améliorer la précision des horloges marines.</p>
                </div>
            </div>

