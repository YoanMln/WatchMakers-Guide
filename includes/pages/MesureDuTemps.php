                    <p class="timeline__content-desc">XVIème siècle</p>
                    <p class="timeline__content-desc"> Galilée aurait découvert, les propriétés du pendule en observant le lustre de la cathédrale de Pise qui Oscillait.</p>
                </div>
            </div>

