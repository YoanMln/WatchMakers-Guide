                    <h2 class="timeline__content-title">Le ressort spiral</h2>
                    <h3 class="timeline__content-date">1675</h3>
                    <p class="timeline__content-desc"> Moins de 20 ans après l'invention du pendule, Christiaan Huygens fait une autre grande découverte pour l'horlogerie. En 1675, il invente le ressort spirale.</p>
                </div>
            </div>

